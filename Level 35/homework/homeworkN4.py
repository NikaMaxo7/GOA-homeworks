#1) Sequencing: ბრძანებები სრულდება ზედა ხაზიდან ქვედა ხაზისკენ
print("Prints This First")
print("Prints This Second")
print("Prints This Third")


#2) Iteration: for ციკლი გამეორებას ასრულებს 5-ჯერ
for i in range(5):
    print("Its going to be printed 5 times")


#3) Selection: პირობის მიხედვით განსხვავებული შედეგი იბეჭდება
temperature = 30

if temperature > 25:
    print("It's hot outside.")
elif temperature > 15:
    print("It's warm outside.")
else:
    print("It's cold outside.")