# When we give 3 arguments to the range() function, they are:
# range(start, end, step)

# Example:
# for i in range(2, 10, 2):
#     print(i)

# This means the variable i starts from 2, goes up to 10 (but does not include 10),
# and increases by 2 each time.
# So it will print: 2, 4, 6, 8

# Explained simply:
# start - where the counting starts from
# end - where it should stop (but this number is not included)
# step - how much to increase each time (or decrease, if negative)

# Yes, this same logic is used in other topics in Python too,
# for example in slicing lists:
# list[1:5:2] â€” this means: start from index 1, go up to 5 (not including), and move by 2 steps

# So the logic of start, end, step works the same in range() and in list slicing.