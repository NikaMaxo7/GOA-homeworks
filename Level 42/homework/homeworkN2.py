my_list = [10, 20, 30, 40, 50]

# 1. Indexing — getting a single element (e.g. the second element)

element = my_list[1] # index 1 means the second element
print(element) # prints: 20

# 2. Slicing — the same as getting a single element, but slicing returns a list

slice_element = my_list[1:2] # starts at index 1 and ends at index 2, 2 is not included
print(slice_element) # prints: [20]