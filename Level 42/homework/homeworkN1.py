# Difference between Mutable and Immutable objects in Python:

# Mutable objects are objects whose values
# can be changed after creation.
# For example: list, dict, set
# my_list = [1, 2, 3]
# my_list[0] = 10 # mutable, now my_list is [10, 2, 3]

# Immutable objects are objects whose values
# cannot be changed after creation.
# For example: int, float, str, tuple
# my_string = "hello"
# my_string[0] = "H" # This will throw an error because str is immutable